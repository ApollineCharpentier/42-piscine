int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	if (nb >= 256)
	{
		while (i <= nb / 16)
		{
			if (nb % i == 0)
				return (0);
			i++;
		}
		return (1);
	}
	else
		while (i < nb)
		{
			if (nb % i == 0)
				return (0);
			i++;
		}
	return (1);
}
